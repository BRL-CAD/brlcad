#include "common.h"

#include "bu.h"


const char *
bu_basename(const char *str)
{
    register const char	*p = str;

    if (UNLIKELY(!str)) {
	return NULL;
    }

    while (*p != '\0')
	if (*p++ == '/')
	    str = p;

    return str;
}

