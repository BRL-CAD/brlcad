#include "common.h"

#include <stdio.h>

#include "bu.h"


int
main(int UNUSED(ac), const char **av)
{
    int status;
    char host[MAXPATHLEN] = {0};

    bu_setprogname(av[0]);

    status = bu_gethostname(host, MAXPATHLEN);
    if (BU_STR_EMPTY(host))
	return 1;

    bu_log("HOST: %s\n", host);
    bu_log("STATUS: %d\n", status);

    return status;
}


