#ifndef MM_CPUINFO_H
#define MM_CPUINFO_H


#include "common.h"


typedef struct {
    unsigned capsse2: 1;
    unsigned capsse3: 1;
    unsigned capsse4p1: 1;
} cpuInfo;


static inline void cpuGetInfo(cpuInfo *cpu)
{
    cpu->capsse2 = cpu->capsse3 = cpu->capsse4p1 = 0;
}


#endif
