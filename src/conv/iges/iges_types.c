#include "./iges_struct.h"
#include "./iges_extern.h"

char *
iges_type(int type)
{
    int i;
    int type_no=0;

    for (i=1; i<ntypes; i++) {
	if (typecount[i].type == type) {
	    type_no = i;
	    break;
	}
    }

    return typecount[type_no].name;
}


